-- =========================================
-- ASSESSMENTS (GRADES) SEED - 4,800 ROWS
-- Deterministic, FK-safe, includes max_score
-- =========================================

INSERT INTO assessments (student_id, class_id, assessment_type, score, max_score, date)
SELECT
  e.student_id,
  e.class_id,
  t.assessment_type,
  ((e.student_id * e.class_id * t.assessment_index) % 41) + 60 AS score,
  t.max_score,
  '2024-10-01' AS date
FROM enrollments e
JOIN (
  SELECT 'quiz' AS assessment_type, 1 AS assessment_index, 25 AS max_score
  UNION ALL
  SELECT 'homework', 2, 35
  UNION ALL
  SELECT 'project', 3, 15
  UNION ALL
  SELECT 'exam', 4, 100
) AS t;


